package com.faculty;

public class Faculty {
	private String facultyId;
	private String title;
	private String name;
	private String course;
	private String facultyLoad;
	private String avlDays;
	
	
	public String getFacultyId() {
		return facultyId;
	}
	public void setFacultyId(String facultyId) {
		this.facultyId = facultyId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCourse() {
		return course;
	}
	public void setCourse(String course) {
		this.course = course;
	}
	public String getAvlDays() {
		return avlDays;
	}
	public void setAvlDays(String avlDays) {
		this.avlDays = avlDays;
	}
	public String getFacultyLoad() {
		return facultyLoad;
	}
	public void setFacultyLoad(String facultyLoad) {
		this.facultyLoad = facultyLoad;
	}	

}
