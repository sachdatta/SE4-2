package com.courses;

public class Courses {
	private String number;
	private String name;
	private String description;
	private String hours;
	private String capacity;
	private String prerequested;
	private String Semester;
	private String fallS;
	private String springS;
	private String summerS;
	private String teacher;
	
	
	public String getTeacher() {
		return teacher;
	}
	public void setTeacher(String teacher) {
		this.teacher = teacher;
	}
	public String getFallS() {
		return fallS;
	}
	public void setFallS(String fallS) {
		this.fallS = fallS;
	}
	public String getSpringS() {
		return springS;
	}
	public void setSpringS(String springS) {
		this.springS = springS;
	}
	public String getSummerS() {
		return summerS;
	}
	public void setSummerS(String summerS) {
		this.summerS = summerS;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String d) {
		this.number = d;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getHours() {
		return hours;
	}
	public void setHours(String hours) {
		this.hours = hours;
	}
	public String getCapacity() {
		return capacity;
	}
	public void setCapacity(String d) {
		this.capacity = d;
	}
	public String getPrerequested() {
		return prerequested;
	}
	public void setPrerequested(String prerequested) {
		this.prerequested = prerequested;
	}
	public String getSemester() {
		return Semester;
	}
	public void setSemester(String semester) {
		Semester = semester;
	}
	

}
