package com.importfile;

public class Student {
	private String studentId;
	private String degreeID;
	private String yearSem;
	
	
	public String getStudentId() {
		return studentId;
	}
	public void setStudentId(String string) {
		this.studentId = string;
	}
	public String getDegreeID() {
		return degreeID;
	}
	public void setDegreeID(String string) {
		this.degreeID = string;
	}
	public String getYearSem() {
		return yearSem;
	}
	public void setYearSem(String yearSem) {
		this.yearSem = yearSem;
	}
	
	
}
