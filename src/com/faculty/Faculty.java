package com.faculty;

public class Faculty {
	private String title;
	private String lastName;
	private String firstName;
	private String grandSchool;
	private String degree;
	private String fallLoad;
	private String springLoad;
	private String summerLoad;
	private String avlDays;
	private String courses;
	
	
	
	
	public String getCourses() {
		return courses;
	}
	public void setCourses(String courses) {
		this.courses = courses;
	}
	public String getDegree() {
		return degree;
	}
	public void setDegree(String degree) {
		this.degree = degree;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getGrandSchool() {
		return grandSchool;
	}
	public void setGrandSchool(String grandSchool) {
		this.grandSchool = grandSchool;
	}
	public String getFallLoad() {
		return fallLoad;
	}
	public void setFallLoad(String fallLoad) {
		this.fallLoad = fallLoad;
	}
	public String getSpringLoad() {
		return springLoad;
	}
	public void setSpringLoad(String springLoad) {
		this.springLoad = springLoad;
	}
	public String getSummerLoad() {
		return summerLoad;
	}
	public void setSummerLoad(String summerLoad) {
		this.summerLoad = summerLoad;
	}
	public String getAvlDays() {
		return avlDays;
	}
	public void setAvlDays(String avlDays) {
		this.avlDays = avlDays;
	}
	
	
}
