package com.degree;

public class Degree {
	private String degreeCode;
	private String degreeDescription;
	private String type;
	private String degreeHours;
	private String degreeCourses;
	public String getDegreeCode() {
		return degreeCode;
	}
	public void setDegreeCode(String degreeCode) {
		this.degreeCode = degreeCode;
	}
	public String getDegreeDescription() {
		return degreeDescription;
	}
	public void setDegreeDescription(String degreeDescription) {
		this.degreeDescription = degreeDescription;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDegreeHours() {
		return degreeHours;
	}
	public void setDegreeHours(String degreeHours) {
		this.degreeHours = degreeHours;
	}
	public String getDegreeCourses() {
		return degreeCourses;
	}
	public void setDegreeCourses(String degreeCourses) {
		this.degreeCourses = degreeCourses;
	}
	
	
	

}
